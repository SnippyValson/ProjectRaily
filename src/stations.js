exports.getStationCode=function(stationName){
    var stationID=-1;
    stations.forEach(function(value){
        if(value['name'].toUpperCase()==stationName.toUpperCase())
        {
            stationID=value['code'];
        }
    });
    return stationID;
};

exports.getStationName=function(stationCode){
    var stationName=-1;
    stations.forEach(function(value){
        if(value['code'].toUpperCase()==stationCode.toUpperCase())
        {
            stationName=value['name'];
        }
    });
    return stationName;
};

exports.fillStationCodesTrainName=function(trainName){
    trainName=trainName.replace(" EXPRESS"," ###");
    trainName=trainName.replace(" EXP"," EXPRESS");
    trainName=trainName.replace(" ###"," EXPRESS");
    trainName=trainName.replace(" SP"," SPECIAL");
    trainName=trainName.replace(" SHTBDI"," SHATABDI");
    trainName=trainName.replace(" PASSR"," PASSENGER");

    var finalTrainName=trainName;
    var myarr = trainName.split(" ");
    var fname= myarr[0].split("-");
    var i;
    if(fname[1]!=undefined)
    {
        stations.forEach(function(value){
            if(value['code'].toUpperCase()==fname[0].toUpperCase())
            {
                fname[0]=value['name'];
            }
            else if(value['code'].toUpperCase()==fname[1].toUpperCase())
            {
                fname[1]=value['name'];
            }
        });
        finalTrainName=fname[0]+"-"+fname[1];
        for(i=1;i<myarr.length;i++)
        {
            finalTrainName+=" "+myarr[i];
        }

    }
    return finalTrainName;
};
